#include <stdio.h>

int	ft_sqrt(int nb)
{
	int	x;

	x = 0;
	while (46360 >= x)
	{
		if ((x * x) == nb)
			return (x);
		x++;
	}
	return (0);
}

int	main()
{
	printf("%d", ft_sqrt(2147395600));
}

